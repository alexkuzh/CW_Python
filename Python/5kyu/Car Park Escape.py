def escape(carpark):
    moves = []
    # print(carpark)
    if carpark[-1][-1] == 2:
        return moves

    for k in range(len(carpark)):
        if carpark[k].count(2) == 1:
            break

    car = carpark[k].index(2)
    d = 0
    for i in range(k,len(carpark) - 1):
        floor = carpark[i]
        stair = floor.index(1)
        # print(f'{floor} stair={stair}, car={car} d={d}')
        if stair < car:
            if d > 0:
                moves.append(f'D{d}')
                d = 0
            moves.append(f'L{car - stair}')
            d += 1
        elif stair > car:
            if d > 0:
                moves.append(f'D{d}')
                d = 0
            moves.append(f'R{stair - car}')
            d += 1
        else:
            d += 1
        car = stair
    if d > 0:
        moves.append(f'D{d}')
    if len(carpark[-1]) - car != 1:
        moves.append(f'R{len(carpark[-1]) - car - 1}')
    return moves


